package com.model;

/**
 *
 * @author Alan Franco
 */
public class Admin {
    
    private String usuario, nombre, pass;

    public Admin(String usuario, String nombre, String pass) {
        this.usuario = usuario;
        this.nombre = nombre;
        this.pass = pass;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
    
    
}
